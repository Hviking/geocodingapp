
package com.beyondb.bean;

import java.io.File;

/**
 * GJB 数据属性
 * @author guanml
 */
public class GJBInfo {
    private  int tf_id;
    private String tf_name;
    private int tf_scale;
    private String tf_envelope;
    private String tf_scale_name;
    private File tf_sms;

    public int getTf_id() {
        return tf_id;
    }

    public void setTf_id(int tf_id) {
        this.tf_id = tf_id;
    }

    public String getTf_name() {
        return tf_name;
    }

    public void setTf_name(String tf_name) {
        this.tf_name = tf_name;
    }

    public int getTf_scale() {
        return tf_scale;
    }

    public void setTf_scale(int tf_scale) {
        this.tf_scale = tf_scale;
    }

    public String getTf_envelope() {
        return tf_envelope;
    }

    public void setTf_envelope(String tf_envelope) {
        this.tf_envelope = tf_envelope;
    }

    public File getTf_sms() {
        return tf_sms;
    }

    public void setTf_sms(File tf_sms) {
        this.tf_sms = tf_sms;
    }

    public String getTf_scale_name() {
        return tf_scale_name;
    }

    public void setTf_scale_name(String tf_scale_name) {
        this.tf_scale_name = tf_scale_name;
    }

     
}
